export interface MachineSpec {
  label: string;
  value: string;
}

export interface CapacityTable {
  headers: string[];
  rows: string[][];
}

export interface SubProduct {
  id: string;
  name: string;
  shortDesc: string;
  image: string;
  features: string[];
  specifications: MachineSpec[];
  capacityTable?: CapacityTable;
  partsTypeId?: string;
  schilt?: boolean;
  /** Hero layout: industrial HUD-style cover for automation / PLC lines */
  heroPreset?: "industrial-automation";
  /** Cover + optional gallery thumbnails on `/machines/rms-controller/...` use the catalog two-column hero; other lines need 2+ entries here for that layout. Omit on RMS controllers for a single listing image. */
  imageGallery?: string[];
  /** Gallery framing: `studio` = soft radial backdrop + shadow for cut-out / transparent PNGs */
  galleryPresentation?: "default" | "studio";
  /** Carousel initial slide when `cover` differs from `[0]` of `imageGallery` (after reordering thumbnails). Default: open on listing `image`. */
  galleryLanding?: "cover" | "first";
  /** Listing card crops: CSS `object-position` for wide/portrait originals (e.g. `right center`). */
  coverObjectPosition?: string;
  /** Listing + cinematic hero fit. `contain` = full PNG visible · `cover`/`bleed` = photo fill (`bleed` = tall hero + oversized crop so wide close-ups reach the edges). */
  coverObjectFit?: "cover" | "contain" | "bleed";
  /** With `contain`, drop horizontal padding so the image spans the full width of the frame (height unchanged). */
  containFlushX?: boolean;
  /** With default cinematic `cover` hero: fixed scale substitute for Ken Burns — values >1 crop soft edges/vignettes (outer box unchanged). */
  coverCropZoom?: number;
  /** Lineup card on `/machines/:line`: match `contain`-style tall image frame (`h-56` otherwise). */
  listingMediaTall?: boolean;
  /** Embedded product video on `/machines/:line/:subId` (YouTube watch URL video id, e.g. `cR2KiIm8DSY`). */
  youtubeVideoId?: string;
}

export interface Machine {
  id: string;
  name: string;
  shortDesc: string;
  image: string;
  models: string[];
  features: string[];
  description: string;
  subProducts?: SubProduct[];
  /** Lineup card: `object-cover` + scale to trim soft borders on full-bleed photos (outer box unchanged). */
  lineCardCoverZoom?: number;
  /** With `lineCardCoverZoom`, CSS `object-position` for off-center compositions (e.g. `right center`). */
  lineCardCoverObjectPosition?: string;
}

